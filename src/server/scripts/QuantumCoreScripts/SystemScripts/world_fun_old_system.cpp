#define GOSSIP_GERTIS "Hello, please help me to come down!"

class npc_gertis : public CreatureScript
{
public:
    npc_gertis() : CreatureScript("npc_gertis") {}

	bool OnGossipHello(Player* player, Creature* creature)
	{
		if (!player->HasSpell(SPELL_FLYING_BATTLE_TANK) && !player->HasItemCount(ITEM_QIRAJ_BATTLE_TANK, 1, true))
			player->ADD_GOSSIP_ITEM(GOSSIP_ICON_CHAT, GOSSIP_GERTIS, GOSSIP_SENDER_MAIN, GOSSIP_ACTION_INFO_DEF+1);

		player->SEND_GOSSIP_MENU(player->GetGossipTextId(creature), creature->GetGUID());
		return true;
	}

	bool OnGossipSelect(Player* player, Creature* creature, uint32 /*sender*/, uint32 action)
    {
        player->PlayerTalkClass->ClearMenus();

		if (action == GOSSIP_ACTION_INFO_DEF+1)
		{
			player->AddItem(ITEM_QIRAJ_BATTLE_TANK, 1);
			player->CastSpell(player, SPELL_HASTE, true);
			player->CastSpell(player, SPELL_SLOW_FALL, true);
			player->CastSpell(player, SPELL_GHOST_WALK, true);
			DoSendQuantumText(SAY_GERTIS_GOODBYE, creature, player);
			player->CLOSE_GOSSIP_MENU();
        }
		return true;
	}

    struct npc_gertisAI : public QuantumBasicAI
    {
        npc_gertisAI(Creature* creature) : QuantumBasicAI(creature){}

		uint32 BuffTimer;

        void Reset()
        {
			BuffTimer = 0.5*IN_MILLISECONDS;

			DoCast(me, SPELL_SPOTLIGHT, true);

			me->SetUInt32Value(UNIT_FIELD_BYTES_1, FIELD_BYTE_1_SITTING);
        }

        void UpdateAI(uint32 const diff)
        {
			if (BuffTimer <= diff && !me->IsInCombatActive())
			{
				DoCast(me, SPELL_FLASK_OF_FROST_WYRM, true);
				DoCast(me, SPELL_HOLY_CHAMPION, true);
				DoCast(me, SPELL_LIGHTNING_SHIELD, true);
				BuffTimer = 10*MINUTE*IN_MILLISECONDS;
			}
			else BuffTimer -= diff;

            if (!UpdateVictim())
                return;

			DoMeleeAttackIfReady();
        }
    };

    CreatureAI* GetAI(Creature* creature) const
    {
        return new npc_gertisAI(creature);
    }
};

