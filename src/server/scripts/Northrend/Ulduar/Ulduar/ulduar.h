class PlayerOrPetCheck
{
public:
	bool operator() (Unit* unit)
	{
		if (unit->GetTypeId() != TYPE_ID_PLAYER)
			if (!unit->ToCreature()->IsPet())
				return true;

		return false;
	}
};

class NoPlayerOrPetCheck
{
public:
	bool operator() (Unit* unit)
	{
		if (unit->GetTypeId() != TYPE_ID_PLAYER)
			if (!unit->ToCreature()->IsPet())
				return false;

		return true;
	}
};

